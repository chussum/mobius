import Foundation

/// 임계값 선제 전환의 **활성 계정 사용량 폴링 서킷 브레이커** 판정.
///
/// 5분마다 도는 사용량 조회가 네트워크 이상(연결 끊김/타임아웃/5xx 등)으로 연속 실패하면,
/// 계속 5분마다 엔드포인트를 두드리는 건 낭비이고 애초에 이 폴링을 옵트인으로 조심스럽게
/// 만든 취지(계정 리스크 최소화)에도 어긋난다. 게다가 네트워크가 이상하면 미리 전환 자체가
/// 무의미하다 — 사용량도 못 읽고, 폴백 검증도 못 하고, Claude 자체도 안 될 테니.
/// → **연속 실패 임계값에 도달하면 배경 폴링을 멈춘다.** 재개는 팝오버를 다시 열 때(사용자가
/// 있고 네트워크가 돌아왔을 가능성) 또는 앱 재시작(카운터가 인메모리)으로만 한다(사용자 결정).
///
/// 판정은 단순 임계값 비교뿐이지만, 상수와 규칙을 한곳에 두고 테스트로 못박아 둔다
/// (AppState는 XCTest 타깃이 없어 오케스트레이션은 못 잡지만, 이 임계값만은 회귀 방지).
public enum UsagePollBreaker {
    /// 서킷을 여는(폴링을 멈추는) 연속 실패 횟수.
    public static let failureThreshold = 3

    /// 서킷이 열렸는가 = 배경 폴링을 멈춰야 하는가. 연속 실패가 임계값 이상이면 true.
    /// 성공 시 호출자가 카운터를 0으로 리셋하므로, 한 번이라도 성공하면 다시 false가 된다.
    public static func isTripped(consecutiveFailures: Int) -> Bool {
        consecutiveFailures >= failureThreshold
    }
}
